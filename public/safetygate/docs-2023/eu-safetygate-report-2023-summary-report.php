    <h2>Responsible Party</h2>
    <p>The term "responsible party" refers to the entity accountable for the product safety issue. The identification of
      the responsible party can occur
      through two distinct pathways: voluntary or compulsory. A voluntary identification occurs when the responsible
      party, such as a manufacturer, importer,
      or distributor, proactively raises a safety alert regarding their product. This demonstrates a commitment to
      consumer safety and regulatory compliance,
      allowing for swift action to mitigate potential risks. On the other hand, compulsory identification happens
      through regulatory action, where authorities,
      during routine inspections or investigations, identify the responsible party and mandate corrective measures.</p>
