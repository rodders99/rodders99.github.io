    <h2>Action Required</h2>
    <p>The term "action required" delineates the measures that need to be taken to address identified safety concerns.
      These actions can be either voluntary or compulsory.
      Voluntary actions are initiated by the responsible party, such as the manufacturer, importer, or distributor, who,
      upon recognizing a potential safety issue,
      proactively undertakes corrective measures. These may include product recalls, issuing safety warnings, or making
      design changes to enhance product safety.
      Voluntary actions reflect the responsible party's commitment to consumer safety and regulatory compliance. </p>
    <p>Conversely, compulsory actions are mandated by regulatory authorities following inspections, market surveillance,
      or consumer complaints. </p>
