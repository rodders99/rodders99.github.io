    <h2 id="cosmetics">What's happening with Cosmetics?</h2>

    <H3 id="cosmetics_ban">European Ban on Lilial in Cosmetics - March 2021</H3>
    <p>2023 was an exception with the introduction of the ban on the chemical <a
        href="https://en.wikipedia.org/wiki/Lilial">lilial / BMHCA</a> for use in cosmetics on 1st March 2022.</p>
    
        
  
      <p>The significant increase seen 2023 over 2022 is due to a change in regulations for a Butylphenyl Methylpropional
              (BMHCA, trade name 'Lilial') BMHCA, a chemical commonly used in cosmetics.</p>
            <p>
      BMHCA is now deemed to be hazardous and unsuitable for use in cosmetic products due to the fact that it is
      included in so many products.
      While an individual exposure poses a minimal risk, cumulative exposure from the use of multiple products
      containing this chemical increases the risk considerably. </p>
    <p>
      It's also worth noting that BMHCA is a fragrance commonly used in household cleaning products, however no guidance
      has been issued for the it's use in cleaning products at this stage.
    </p>

    <div style="background-color: white; display: inline-block; ">
      <img src="/safetygate/img-2024/icons/bmhca-molecule.png"><br> BMHCA "Lilial" Molecule
    </div>


    <p>
      Since 1 March 2022, butylphenyl methylpropional (BMHCA/'Lilial') is classified as a Carcinogenic, Mutagenic or
      Reprotoxic (CMR) category 1B substance under the EU and GB Classification,
      Labelling and Packaging (CLP) Regulations.</p>
    <p>
      This ban affected around 1,100 - 1,200 products (90-95% of the annual increase of (1,249) alerts or 32-35% of all
      alerts issued (3,349) in 2023), and is the reason for the
      significant increase seen in the Safetygate analysis.</p>

