<h1 id="common-themes">Common Themes</h1>

<p class='last-updated'><?php include '../tables/last-updated.php' ?></p>

  <p>Based on the data analysis conducted on mainstream media headlines regarding electric vehicles (EVs), several recurring themes have emerged. 
    A significant portion of the headlines focuses on sensational and often unsubstantiated claims. For instance :</p> 
    <ul>
    <li>There are numerous articles suggesting 
    that China is either spying on or remotely controlling EVs, despite a lack of concrete evidence supporting these assertions.</li> 
    <li>Additionally, there is a 
    prevalent narrative around EVs catching fire. This is particularly misleading, given that data indicates EVs are actually 20 times less likely to catch 
    fire compared to internal combustion engine (ICE) vehicles. Despite this, the media continues to highlight these rare incidents, contributing to an 
    inflated perception of risk.</li>

  <li>Another common theme is the proliferation of negative articles from various perspectives. Subject matter experts frequently voice scepticism about 
    EV technology and its feasibility, often without considering the full breadth of available data.</li> 
    <li>Personal experience stories also skew the negative, 
    with individuals recounting their dissatisfaction or issues with EVs. These personal anecdotes are often given undue weight, overshadowing broader 
    positive trends.</li> 
    <li>Additionally, there are repeated reports of problems obtaining planning permission for new charging locations, which paints a 
    picture of infrastructural challenges that may deter potential EV buyers.</li> 
  <li>Headlines also suggest falling sales figures, though these claims often lack context or are not representative of the overall market trajectory.</li> 
  <li>Furthermore, there are general warnings for EV drivers that seem to lack substantive backing, further fuelling public uncertainty.</li>
  </ul>

