<h1>Economic Area of Product origin</h1>

<p>This is an engineered feature from the data, this splits results by Economic Area which groups the countries under : </h1>

<ul>
  <li><code>EU Countries</code> within Schengen or European Economic Area (Ex Italy for visibility of 2023 alerts)</li>
  <li><code>Italy</code> - Italy represents around 30% of all alerts issued in 2023</li>
  <li><code>Outside EU</code> - Every other country not within the Eu Countries</li>
  <li><code>China</code> - because China represents such a large proportion of Global Trade</li>
  <li><code>Unknown</code> Country of origin has been excluded</li>
</ul>



